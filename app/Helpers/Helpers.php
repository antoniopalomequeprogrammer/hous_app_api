    public static function sum($values){
        $total = 0;
        foreach ($values as $value) {
            if ($value) {
                $total += (float) $value;
            }
        }
        return $total;
    }

    public static function sumMultiple($values){
        $total = 0;
        foreach ($values as $value) {
            foreach ($value as $v) {
                if ($v['valor']) {
                    $total += (float) $v['valor'];
                }
            }
        }
        return $total;
    }
